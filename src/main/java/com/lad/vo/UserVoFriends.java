package com.lad.vo;


public class UserVoFriends {
	private String id;

	private String userName;

	private String phone;

	private String sex;

	private String headPictureName;

	private String birthDay;

	private String friendsTableId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getHeadPictureName() {
		return headPictureName;
	}

	public void setHeadPictureName(String headPictureName) {
		this.headPictureName = headPictureName;
	}

	public String getBirthDay() {
		return birthDay;
	}

	public void setBirthDay(String birthDay) {
		this.birthDay = birthDay;
	}

	public String getFriendsTableId() {
		return friendsTableId;
	}

	public void setFriendsTableId(String friendsTableId) {
		this.friendsTableId = friendsTableId;
	}
}
